type Service = keyof typeof SERVICES

interface Props {
  className?: string
}

const Social = ({ className = '' }: Props): JSX.Element => {
  const data: SiteMetadata = useStaticQuery(graphql`
    query {
      ...SiteMetadata
    }
  `)

  const {
    site: {
      siteMetadata: { social },
    },
  } = data

  return (
    <ul className={`${className} social`}>
      {Object.keys(social).map((service: string) => {
        const username = social[service as Service]
        const { url, icon } = SERVICES[service as Service]
        return (
          <li className="social__item" key={service}>
            <a
              className="social__icon"
              href={url.replace('<username>', username)}
            >
              {icon}
            </a>
          </li>
        )
      })}
    </ul>
  )
}

